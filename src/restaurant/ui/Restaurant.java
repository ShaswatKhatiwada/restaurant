    private void jButtonDeleteCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteCategoryActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this category. "
                + "This will delete all food items related to this.", "Restaurant", JOptionPane.YES_NO_CANCEL_OPTION);
        if (JOptionPane.YES_OPTION == confirm) {
            categories.deleteByName(jListCategories.getSelectedValue().toString());
            rebuildCategoryList();
        }
    }//GEN-LAST:event_jButtonDeleteCategoryActionPerformed

    private void jButtonSearchCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchCategoryActionPerformed
        if (jTextFieldSearchCategory.getText().isEmpty()) {
            rebuildCategoryList();
            return;
        }
        LinkedList<CategoryNode> result = categories.search(jTextFieldSearchCategory.getText().trim());
        listModel.clear();
        for (CategoryNode c : result) {
            listModel.addElement(c.getName());
        }
    }//GEN-LAST:event_jButtonSearchCategoryActionPerformed

