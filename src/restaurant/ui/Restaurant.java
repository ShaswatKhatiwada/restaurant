    void updateCategoryNode(String oldName, String newName) {
        categories.updateByName(oldName, newName);
        if (currentCategoryNode != null) {
            rebuildCategoryList();
        }
    }

    private void rebuildCategoryList() {
        listModel.clear();
        for (CategoryNode c : categories.getCategories()) {
            listModel.addElement(c.getName());
        }
    }
